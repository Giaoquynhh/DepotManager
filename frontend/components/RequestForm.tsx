import { useState } from 'react';
import { api } from '@services/api';

interface RequestFormProps {
  onSuccess: () => void;
  onCancel: () => void;
}

export default function RequestForm({ onSuccess, onCancel }: RequestFormProps) {
  const [form, setForm] = useState({ 
    type: 'IMPORT', 
    container_no: '', 
    etaDate: '', 
    etaTime: ''
  });
  const [selectedFile, setSelectedFile] = useState<File | null>(null);
  const [loading, setLoading] = useState(false);
  const [message, setMessage] = useState('');

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    setMessage('');

    // Validation d·ª±a tr√™n lo·∫°i y√™u c·∫ßu
    if (form.type === 'IMPORT') {
      if (!form.container_no.trim()) {
        setMessage('M√£ ƒë·ªãnh danh container l√† b·∫Øt bu·ªôc cho y√™u c·∫ßu nh·∫≠p');
        setLoading(false);
        return;
      }
      if (!selectedFile) {
        setMessage('Ch·ª©ng t·ª´ l√† b·∫Øt bu·ªôc cho y√™u c·∫ßu nh·∫≠p');
        setLoading(false);
        return;
      }
    }
    
    if (!form.etaDate || !form.etaTime) {
      setMessage('Th·ªùi gian d·ª± ki·∫øn (ETA) l√† b·∫Øt bu·ªôc');
      setLoading(false);
      return;
    }

    try {
      // T·∫°o FormData ƒë·ªÉ upload file
      const formData = new FormData();
      formData.append('type', form.type);
      
      // Ch·ªâ g·ª≠i container_no v√† document cho lo·∫°i IMPORT
      if (form.type === 'IMPORT') {
        formData.append('container_no', form.container_no);
        if (selectedFile) {
          formData.append('document', selectedFile);
        }
      }
      
      // ETA lu√¥n ƒë∆∞·ª£c g·ª≠i cho c·∫£ 2 lo·∫°i
      if (form.etaDate && form.etaTime) {
        const etaDateTime = `${form.etaDate}T${form.etaTime}`;
        formData.append('eta', etaDateTime);
      }

      await api.post('/requests', formData, {
        headers: {
          'Content-Type': 'multipart/form-data',
        },
      });
      
      setMessage('ƒê√£ t·∫°o y√™u c·∫ßu th√†nh c√¥ng!');
      setTimeout(() => {
        onSuccess();
      }, 1000);
    } catch (error: any) {
      setMessage(error?.response?.data?.message || 'C√≥ l·ªói x·∫£y ra');
    } finally {
      setLoading(false);
    }
  };

  const handleFileChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0];
    if (file) {
      // Ki·ªÉm tra ƒë·ªãnh d·∫°ng file - ki·ªÉm tra c·∫£ MIME type v√† extension
      const allowedMimeTypes = ['application/pdf', 'image/jpeg', 'image/png', 'image/jpg'];
      const allowedExtensions = ['.pdf', '.jpg', '.jpeg', '.png'];
      
      const fileExtension = file.name.split('.').pop()?.toLowerCase();
      const hasValidMimeType = allowedMimeTypes.includes(file.type);
      const hasValidExtension = fileExtension && allowedExtensions.includes(`.${fileExtension}`);
      
      if (!hasValidMimeType && !hasValidExtension) {
        setMessage('Ch·ªâ ch·∫•p nh·∫≠n file PDF ho·∫∑c ·∫£nh (JPG, PNG)');
        return;
      }
      
      // Ki·ªÉm tra k√≠ch th∆∞·ªõc file (max 10MB)
      if (file.size > 10 * 1024 * 1024) {
        setMessage('File qu√° l·ªõn. K√≠ch th∆∞·ªõc t·ªëi ƒëa l√† 10MB');
        return;
      }
      
      setSelectedFile(file);
      setMessage('');
    }
  };

  const removeFile = () => {
    setSelectedFile(null);
    setMessage('');
  };

  return (
    <form onSubmit={handleSubmit} className="request-form">
      <div className="form-group">
        <label htmlFor="type">Lo·∫°i y√™u c·∫ßu</label>
        <select 
          id="type"
          value={form.type} 
          onChange={e => setForm({...form, type: e.target.value})}
          required
        >
          <option value="IMPORT">Nh·∫≠p</option>
          <option value="EXPORT">Xu·∫•t</option>
        </select>
      </div>

      {/* Lu·ªìng Nh·∫≠p - hi·ªÉn th·ªã khi type = IMPORT */}
      {form.type === 'IMPORT' && (
        <>
          <div className="form-group">
            <label htmlFor="container_no">M√£ ƒë·ªãnh danh container <span className="required">*</span></label>
            <input 
              id="container_no"
              type="text"
              placeholder="Nh·∫≠p m√£ container..." 
              value={form.container_no} 
              onChange={e => setForm({...form, container_no: e.target.value})}
            />
          </div>

          <div className="form-group">
            <label htmlFor="document">Ch·ª©ng t·ª´ (PDF/·∫¢nh) <span className="required">*</span></label>
            <div className="file-upload-container">
              <input
                id="document"
                type="file"
                accept=".pdf,.jpg,.jpeg,.png"
                onChange={handleFileChange}
                className="file-input"
              />
              <label htmlFor="document" className="file-upload-label">
                <span className="file-upload-icon">üìé</span>
                <span className="file-upload-text">
                  {selectedFile ? selectedFile.name : 'Ch·ªçn file ch·ª©ng t·ª´...'}
                </span>
              </label>
            </div>
            {selectedFile && (
              <div className="file-preview">
                <span className="file-name">{selectedFile.name}</span>
                <button 
                  type="button" 
                  onClick={removeFile}
                  className="file-remove"
                >
                  ‚úï
                </button>
              </div>
            )}
            <small className="file-hint">
              ƒê·ªãnh d·∫°ng: PDF, JPG, PNG. K√≠ch th∆∞·ªõc t·ªëi ƒëa: 10MB
            </small>
          </div>
        </>
      )}

      {/* ETA - hi·ªÉn th·ªã cho c·∫£ 2 lo·∫°i */}
      <div className="form-group">
        <label htmlFor="eta">Th·ªùi gian d·ª± ki·∫øn (ETA) <span className="required">*</span></label>
        <div className="eta-inputs">
          <div className="eta-date">
            <input 
              id="etaDate"
              type="date" 
              value={form.etaDate} 
              onChange={e => setForm({...form, etaDate: e.target.value})}
              required
            />
          </div>
          <div className="eta-time">
            <input 
              id="etaTime"
              type="time" 
              value={form.etaTime} 
              onChange={e => setForm({...form, etaTime: e.target.value})}
              required
            />
          </div>
        </div>
      </div>

      {message && (
        <div className={`form-message ${message.includes('th√†nh c√¥ng') ? 'success' : 'error'}`}>
          {message}
        </div>
      )}

      <div className="form-actions">
        <button 
          type="button" 
          className="btn btn-outline" 
          onClick={onCancel}
          disabled={loading}
        >
          H·ªßy
        </button>
        <button 
          type="submit" 
          className="btn btn-primary" 
          disabled={loading}
        >
          {loading ? 'ƒêang t·∫°o...' : 'T·∫°o y√™u c·∫ßu'}
        </button>
      </div>
    </form>
  );
}
